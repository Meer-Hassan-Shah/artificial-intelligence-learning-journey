#Python Operators


# mathamatically operator
print("---------------mathamatically operator--------------------")
print(17 + 13)   #addition
print(17 - 7)    #substraction 

print(5 * 10)    #multiply
print(15 / 3)    #divide
print(17 // 3)   #floor divide

print(10 % 3)    #remainder
print(2 ** 4)    #exponent OR power 2^4 


#comperasion operator
print("---------------comperasion operator--------------------")
print(5  == 5)   #equality
print(7 != 5)    #unequality
print(10 > 2)    #greater than
print(4 < 3)     #less than


#binary operator

print("---------------binary operator--------------------")
print(True and False)   # binary "AND"  operator
print(False or True)    # binary "OR"  operator
print(not False)         # binary "NOT"  operator


#Assignment operator
print("---------------Assignment operator--------------------")

x = 10        # simple assignment
print(x)
x += 5        # same as x = x + 5
print(x)
x -= 3        # same as x = x - 3
print(x)
x *= 2        # same as x = x * 2
print(x)
x /= 4        # same as x = x / 4
print(x)
